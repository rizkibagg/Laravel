     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $dosen = [
            'nik' => $request->nik,
            'nama' => $request->nama,
            'bidang' => $request->bidang,
            'status' => $request->status,
        ];
        Dosen::create($dosen);
        return redirect('Dashboard');
    }

